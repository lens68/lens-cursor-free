#!/usr/bin/env node
/** Generate shared/app-version.ts from package.json version. */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const pkg = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'));
const version = String(pkg.version ?? '0.0.0');
const out = join(root, 'shared', 'app-version.ts');
const body = `/** Auto-generated by scripts/gen-app-version.mjs — do not edit by hand. */\nexport const APP_VERSION = ${JSON.stringify(version)};\n`;
writeFileSync(out, body, 'utf8');
console.log(`wrote ${out} (${version})`);

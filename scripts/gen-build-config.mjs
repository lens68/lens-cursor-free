#!/usr/bin/env node
/** Generate shared/build-config.ts from build-config.json (CI can replace JSON per channel). */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const cfg = JSON.parse(readFileSync(join(root, 'build-config.json'), 'utf8'));
const hub = String(cfg.defaultHubUrl ?? 'http://127.0.0.1:8765');
const rotate = cfg.autoStartupRotate !== false;
const productDisplayName = String(cfg.productDisplayName ?? 'Relay');
const productTagline = String(cfg.productTagline ?? '账号切换助手');
const supportUrl = String(cfg.supportUrl ?? '');
const purchaseUrl = String(cfg.purchaseUrl ?? '');
const enableOpsTools = cfg.enableOpsTools === true;
const out = join(root, 'shared', 'build-config.ts');
const body = `/** Auto-generated by scripts/gen-build-config.mjs — do not edit by hand. */
export const DEFAULT_HUB_URL = ${JSON.stringify(hub)};
export const AUTO_STARTUP_ROTATE = ${rotate};
export const PRODUCT_DISPLAY_NAME = ${JSON.stringify(productDisplayName)};
export const PRODUCT_TAGLINE = ${JSON.stringify(productTagline)};
export const SUPPORT_URL = ${JSON.stringify(supportUrl)};
export const PURCHASE_URL = ${JSON.stringify(purchaseUrl)};
export const ENABLE_OPS_TOOLS = ${enableOpsTools};
`;
writeFileSync(out, body, 'utf8');
console.log(
  `wrote ${out} (hub=${hub}, ops=${enableOpsTools}, product=${productDisplayName})`,
);
